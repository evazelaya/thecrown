document.addEventListener("DOMContentLoaded", function() {
  const imagenes = document.querySelectorAll('.galeria-imagen');

  imagenes.forEach(imagen => {
    imagen.addEventListener('click', function() {
      // Cambiar el tamaño de la imagen al hacer clic
      if (imagen.style.transform === "scale(1.5)") {
        imagen.style.transform = "scale(1)"; // Restablecer al tamaño original
      } else {
        imagen.style.transform = "scale(1.5)"; // Aumentar la imagen
      }
    });
  });
});



























































































































































