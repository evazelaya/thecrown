document.addEventListener("DOMContentLoaded", () => {
  // Seleccionamos todos los botones "Ver más"
  const botonesVerMas = document.querySelectorAll('.ver-mas');

  // Asignamos un evento de clic a cada botón "Ver más"
  botonesVerMas.forEach(boton => {
    boton.addEventListener('click', () => {
      const infoId = boton.getAttribute('data-info'); // Obtenemos el id del contenedor de información
      const info = document.getElementById(infoId + '-info'); // Usamos el id para mostrar la info correspondiente
      // Mostrar u ocultar la información del personaje
      info.style.display = (info.style.display === 'none' || info.style.display === '') ? 'block' : 'none';
    });
  });
});

































































































































































































































